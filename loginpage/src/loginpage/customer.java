       private void table_update(){
        int CC;
        try{
            
   pst = con.prepareStatement("SELECT * FROM custdetails");
   //pst = con.prepareStatement("SELECT * FROM asmscustomer");
   ResultSet Rs = pst.executeQuery();
   ResultSetMetaData RSMD = Rs.getMetaData();
   CC = RSMD.getColumnCount();
   DefaultTableModel DFT = (DefaultTableModel) jTable2.getModel();
   DFT.setRowCount(0);
            
            while (Rs.next()){
                Vector v2 = new Vector();
                
                for (int i1=1;i1<=CC;i1++){
                    v2.add(Rs.getString("cno"));
                       v2.add(Rs.getString("cname"));
                          v2.add(Rs.getString("mobileno"));
                             v2.add(Rs.getString("address"));
                                v2.add(Rs.getString("spid"));
                                    v2.add(Rs.getString("spname"));
                                         v2.add(Rs.getString("qty"));
                }
                DFT.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }    
   
        
     
