    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //insert button
        try {
            String cname,address,spid,spname,qty;
            
            cname=txtcname.getText();
           address=txtaddr.getText();
           spid = txtspid.getText();
           //qty=txtqty.getText();
           spname=txtspname.getText();
            qty= txtqty.getText();
            String contactno = txtphno.getText();
            
        
        if(contactno.matches("^[0-9]*$") && contactno.length()==10)
        {
           JOptionPane.showMessageDialog(null,"Correct mobile no");
      
            //contactno=txtcontactno.getText();
            //salary=txtsalary.getText();
            pst = con.prepareStatement("insert into custdetails(cname,mobileno,address,spid,spname,qty) values(?,?,?,?,?,?)");
            //pst = con.prepareStatement("insert into asmscustomer(cname,mobileno,address,spid,spname,qty) values(?,?,?,?,?,?)");
            
            pst.setString(1,cname);
             pst.setString(2,contactno);
            pst.setString(3,address);
            pst.setString(4,spid);
            pst.setString(5,spname);
           
           pst.setString(6,qty);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Record Saved");
            table_update();
            
            txtcname.setText("");
            txtphno.setText("");
            txtaddr.setText("");
            txtspid.setText("");
            txtspname.setText("");
            txtqty.setText("");
          //  txtspname.requestFocus();
           // txtqty.setText("");
            txtcname.requestFocus();
        }
        else
            JOptionPane.showMessageDialog(null,"Invalid mobile no");
                  
        } catch (SQLException ex) {
            Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
        } 
    
                                      

    }//GEN-LAST:event_jButton2ActionPerformed

